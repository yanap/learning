# encoding: utf-8
a = 1
b = "str"
# 配列リテラル
c = [a, b, 3, "文字列"] #=> [1, "str", 3, "文字列"]
# ネストもできる
d = [a, c, [1, 2, 3]] #=> [1, [1, "str", 3, "文字列"], [1, 2, 3]]

#2-6
p c[0..1] #=> [1, "str"] 末端を含む範囲
p c[0...1] #=> [1]
p c[-2..-1] #=> [3, "文字列"] 負の添字との組み合わせ
p c[-2..3] #=> [3, "文字列"]
p c[-2...3] #=> [3] 末端を含まない範囲
p c[4..5] #=> [] 配列の範囲外

